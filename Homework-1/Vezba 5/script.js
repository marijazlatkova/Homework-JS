// The Temperature Converter

var celsius = 40;

var celsiusInFahrenheit = (celsius * 9) / 5 + 32;

var result_celsiusInFahrenheit = celsius + '°C is ' + celsiusInFahrenheit + '°F.';

var result_celsiusInFahrenheit = `${celsius}°C is ${celsiusInFahrenheit}°F.`;

console.log(result_celsiusInFahrenheit);

var fahrenheit = 86;

var fahrenheitInCelsius = (fahrenheit - 32) * 5 / 9;

var result_fahrenheitInCelsius = fahrenheit + '°F is ' + fahrenheitInCelsius + '°C.';

var result_fahrenheitInCelsius = `${fahrenheit}°F is ${fahrenheitInCelsius}°C.`;

console.log(result_fahrenheitInCelsius);

